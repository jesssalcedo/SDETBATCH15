package Class2;

public class VariableValues {
    public static void main(String[] args) {
        /*
        Create a Java program and name it VariableValues
In your program create variables to store all different types of primitive data.
Update(reassign) value of each variable
Print the value of each variable
         */
        byte box1=15;
        box1=25;
        short box2=250;
        box2=500;
        int box3=10000;
        box3=20000;
        long box4=20000000000l;
        box4=40000000000l;
        char box5='F';
        box5='T';
        boolean female=true;
        female=false;
        String box6="Jessica";
        box6="Salcedo";

        System.out.println(box1);
        System.out.println(box2);
        System.out.println(box3);
        System.out.println(box4);
        System.out.println(box5);
        System.out.println(box6);
        System.out.println(female);

    }
}
